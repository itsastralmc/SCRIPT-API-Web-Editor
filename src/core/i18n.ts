const ALL_TRANSLATIONS: Record<LangCode, Translations> = { en, es, pt, ja, zh, it, de, ru, ko };

let currentLang: LangCode = (localStorage.getItem('itsastralmc-lang') as LangCode) || 'en';
const listeners: Array<() => void> = [];

/** Get a translated string */
export function t(key: TranslationKey): string {
  return ALL_TRANSLATIONS[currentLang]?.[key] ?? ALL_TRANSLATIONS.en[key] ?? key;
}

/** Get the current language code */
export function getLang(): LangCode {
  return currentLang;
}

/** Set the language and notify all listeners */
export function setLang(lang: LangCode): void {
  currentLang = lang;
  localStorage.setItem('itsastralmc-lang', lang);
  for (const fn of listeners) fn();
}

/** Subscribe to language changes. Returns an unsubscribe function. */
export function onLangChange(fn: () => void): () => void {
  listeners.push(fn);
  return () => {
    const idx = listeners.indexOf(fn);
    if (idx >= 0) listeners.splice(idx, 1);
  };
}